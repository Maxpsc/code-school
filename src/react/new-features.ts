/**
 * React v16 新特性
 */

/**
 * 1. Fiber 架构
 * - 异步渲染架构
 * - 支持 renderToNodeStream
 */

/**
 * 2. Error Boundaries
 * - 错误边界捕获子组件渲染错误
 */

/**
 * 3. Portal
 * - 支持将子组件渲染到父组件 DOM 树之外
 */

/**
 * 4. Context API
 * - 支持全局状态传递
 */

/**
 * 5. Hooks (v16.8)
 * - useState, useEffect, useContext, useReducer
 * - useRef, useMemo, useCallback
 */

/**
 * React v18 新特性
 */

/**
 * 1. Automatic Batching
 * - 自动批量更新状态
 */

/**
 * 2. Concurrent Features
 * - startTransition
 * - useDeferredValue
 * - useTransition
 */

/**
 * 3. Suspense for Data Fetching
 * - 支持 use 关键字
 */

/**
 * 4. 新的 Hooks
 * - useId
 * - useSyncExternalStore
 * - useInsertionEffect
 */

